export function translate(n) {
	if (n === "January") return 'Январь';
	if (n === "February") return 'Февраль';
	if (n === "March") return 'Март';
	if (n === "April") return 'Апрель';
	if (n === "May") return 'Май';
	if (n === "June") return 'Июнь';
	if (n === "July") return 'Июль';
	if (n === "August") return 'Август';
	if (n === "September") return 'Сентябрь';
	if (n === "October") return 'Октябрь';
	if (n === "November") return 'Ноябрь';
	if (n === "December") return 'Ддекабрь';
	return 'You write the wrong month';
}